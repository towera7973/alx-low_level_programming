#include <stdio.h>

/**
 * add - add tow numbers
 * @a: first number
 * @b: second number
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * div - div two numbers
 * @a: first number
 * @b: second number
 * Return: division
 */
int  div(int a, int b)
{ 
	return (a / b);
}

/**
 * mul - multiply two numbers
 * @a: first number
 * @b: second number
 * Return: multiple
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * sub - add tow numbers
 * @a: first number
 * @b: second number
 * Return: difference
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mod - modulas of  two numbers
 * @a: first number
 * @b: second number
 * Return: remendor
 */
int mod(int a, int b)
{
	return (a % b);
}
